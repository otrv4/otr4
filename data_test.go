func (s *OTR4Suite) Test_FromHexChar(c *C) {
	b, valid := fromHexChar(51)
	exp := uint8(0x3)

	c.Assert(b, DeepEquals, exp)
	c.Assert(valid, Equals, true)

	b, valid = fromHexChar('a')
	exp = uint8(0xa)

	c.Assert(b, DeepEquals, exp)
	c.Assert(valid, Equals, true)

	b, valid = fromHexChar('A')
	exp = uint8(0xA)

	c.Assert(b, DeepEquals, exp)
	c.Assert(valid, Equals, true)

	b, valid = fromHexChar('.')
	exp = uint8(0x0)

	c.Assert(b, DeepEquals, exp)
	c.Assert(valid, Equals, false)
}

